    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed
        Calculadora calc = new Calculadora();
        calc.setAlturaAzulejo(Double.parseDouble(heightAzulejoText.getText()));
        calc.setLarguraAzulejo(Double.parseDouble(widthAzulejoText.getText()));
        calc.setAlturaParede(Double.parseDouble(heightWallText.getText()));
        calc.setLarguraParede(Double.parseDouble(widthWallText.getText()));
        calc.setAreaAzulejo(typeCombobox.getSelectedItem().toString());
        DefaultListModel modelList = new DefaultListModel();
        modelList.addElement("Quantidade de azulejos necess√°rios: " + calc.getQuantidadeAzulejos());
        finalList.setModel(modelList);
    }//GEN-LAST:event_calculateButtonActionPerformed

